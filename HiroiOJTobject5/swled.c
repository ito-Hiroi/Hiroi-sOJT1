void Led_switch( void )
{
	if(Led_State == ON ){				//LEDが点灯中なら
		Led_State = OFF;				//LEDを消灯させる
	} else {
		Led_State = ON;					//LEDを点灯させる
		Event_DUTY=MOVEDUTY;
		PWM_NOW = PWM_INCREASE;
	}
}

//*****************************************************************************
/// 
//*****************************************************************************
void Led_Main( void )
{
	if( Led_State == OFF){
		MTU3.TGRC=MTU3.TGRB;		//Duty比を0%にしてLEDをOFFする
	}else{
		//何もしない
	}
}